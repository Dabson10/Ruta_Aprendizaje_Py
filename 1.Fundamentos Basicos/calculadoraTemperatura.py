# Conversor de temperatura de celcius a fahrenheit y al reves

# De Celsius a Fahrenheit

celsius = 40

rFahren = (celsius * (9/5)) + 32

print("La conversion de Celsius a Fahrenheit es de: ",rFahren)

# De Fahrenheit a Celsius 

fahren = 104 

rCelsius = (fahren - 32) * (5/9)
print("La conversion de Fahrenheit a Celsius es de: ",rCelsius)